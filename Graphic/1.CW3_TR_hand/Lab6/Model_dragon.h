void getSpine(std::vector<glm::vec3>& spine_vertices) {
	glm::vec3 spine_pos;

	for (float x = -0.6, y = -0.6; x <= 0.6; x+=0.2, y+=0.2) {

		if (x == -0.6) {
			spine_pos.y = -0.1;
		}
		else if (x == -0.6) {
			spine_pos.y = -0.3;
		}
		else if (x == 0.6) {
			spine_pos.y = 0.3;
		}
		else {
			spine_pos.y = y;
		}
		spine_pos.x = x;
		spine_vertices.push_back(spine_pos);
	}
}
void getLegs(std::vector<glm::vec3>& leg_vertices) {
	glm::vec3 leg_pos[2];

	leg_pos[0].x = leg_pos[1].x = -0.2;
	leg_pos[0].y = leg_pos[1].y = -0.6;
	leg_pos[0].z = 0.2;
	leg_pos[1].z = -0.2;

	leg_vertices.push_back(leg_pos[0]);
	leg_vertices.push_back(leg_pos[1]);
}

void getWings(std::vector<glm::vec3>& wing_vertice) {
	glm::vec3 wing_pos[2];

	wing_vertice.push_back(wing_pos[0]);
	wing_vertice.push_back(wing_pos[1]);
}


